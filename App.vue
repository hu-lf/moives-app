<template>
  <div
    id="app"
    :class="
      typeof weather.main != 'undefined' && weather.main.temp > 16 ? 'warm' : ''
    "
  >
    <main>
      <!-- 天气搜索框 -->
      <div class="search-box">
        <input
          type="text"
          class="search-bar"
          placeholder="Seach..."
          v-model="query"
          @keyup.enter="fetchWeather"
        />
      </div>

      <!-- 天气信息框 -->
      <div class="weather-wrap" v-if="typeof weather.main != 'undefined'">
        <div class="location-box">
          <div class="location">
            {{ weather.name }}, {{ weather.sys.country }}
          </div>
          <div class="date">
            {{ dateBuilder() }}
          </div>
        </div>

        <div class="weather-box">
          <div class="temp">{{ weather.main.temp }}℃</div>
          <div class="weather">{{ weather.weather[0].main }}</div>
        </div>
      </div>
    </main>
  </div>
</template>

<script>
export default {
  name: "App",
  data() {
    return {
      api_key: "283a98a8c351a598c69a9933c245c017",
      url_base: "https://api.openweathermap.org/data/2.5/",
      query: "",
      weather: {},
    };
  },
  methods: {
    // 根据参数拼接url & 请求
    fetchWeather() {
      fetch(
        `${this.url_base}weather?q=${this.query}&units=metric&APPID=${this.api_key}`
      )
        .then((res) => {
          return res.json();
        })
        .then(this.setResults);
    },
    // 异步请求成功后回调该函数
    setResults(results) {
      this.weather = results;
    },
    // 获取当前时间的字符串
    dateBuilder() {
      // 获得现在的时间
      let d = new Date();
      let months = [
        "January",
        "February",
        "March",
        "April",
        "May",
        "June",
        "July",
        "August",
        "September",
        "October",
        "November",
        "December",
      ];
      let days = [
        "Sunday",
        "Monday",
        "Tuesday",
        "Wednesday",
        "Thursday",
        "Friday",
        "Saturday",
      ];
      // 返回 Wednesday
      let day = days[d.getDay()];
      // 返回 24  当前日期
      let date = d.getDate();
      // 返回 February
      let month = months[d.getMonth()];
      // 返回 当前年份
      let year = d.getFullYear();
      return `${day} ${date} ${month} ${year}`;
    },
  },
};
</script>

